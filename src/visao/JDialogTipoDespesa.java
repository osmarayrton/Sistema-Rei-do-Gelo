    private void botPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botPesquisarActionPerformed
        // TODO add your handling code here:
        int linha = -1;
        linha = tabTipoDespesa.getSelectedRow();

        if (linha != -1) {
            tipoDespesa = listaTipoDespesas.get(linha);
            txtBuscarTipoDesp.setText(tipoDespesa.getTpdespDescricao());
        }

        listaTipoDespesas.clear();
        listaTipoDespesas.addAll(controle.consultarPorDescricao(txtBuscarTipoDesp.getText()));
        System.out.println(listaTipoDespesas.size());
        tipoDespesaList.clear();
        tipoDespesaList.addAll(listaTipoDespesas);
    }//GEN-LAST:event_botPesquisarActionPerformed

