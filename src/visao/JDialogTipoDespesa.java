    private void tabTipoDespesaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabTipoDespesaMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount() == 2){
            int linha = tabTipoDespesa.getSelectedRow();
            
            if(linha != -1){
                TipoDespesa tpdesp = tipoDespesaList.get(linha);
                JDialogInserirTipoDespesa tela = new JDialogInserirTipoDespesa(null,true,tpdesp);
                tela.setVisible(true);
                
                tipoDespesaList.clear();
                tipoDespesaList.addAll(controle.listarTodos());
            }
        }
    }//GEN-LAST:event_tabTipoDespesaMouseClicked

