    public void editar() {
        int linha = -1;
        linha = tabUs.getSelectedRow();

        if (linha != -1) {
            Usuario us = usuarioList.get(linha);
            JDialogInserirUsuario tela = new JDialogInserirUsuario(null, true, us);
            tela.setVisible(true);

            usuarioList.clear();
            usuarioList.addAll(controle.listarTodos());
        }
        atualizar();
    }

    public void delete() {
        int resposta;
        int linha, cod;
        linha = tabUs.getSelectedRow();

        if (linha != -1) {
            usuario = usuarioList.get(linha);
            resposta = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente excluir?");
            if (resposta == JOptionPane.YES_OPTION) {
                controle.excluir(usuario);
                usuarioList.clear();
                usuarioList.addAll(controle.listarTodos());
                tabUs.updateUI();
            } else {
                JOptionPane.showMessageDialog(this, "SELECIONE UM CLIENTE NA TABELA PARA EXCLUIR");
            }

        }
    }
