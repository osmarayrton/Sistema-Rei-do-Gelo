    private void botSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botSalvarActionPerformed
        // TODO add your handling code here:
        String senha = passwordSenha.getText();

        MessageDigest md;
        try {
            md = MessageDigest.getInstance("SHA-256");
            try {

                byte password[] = md.digest(senha.getBytes("UTF-8"));

                StringBuilder hexString = new StringBuilder();
                for (byte b : password) {
                    hexString.append(String.format("%02X", 0xFF & b));
                }
                String senhahex = hexString.toString();

                System.out.println(senhahex);
                usuario.setUsSenha(senhahex);

            } catch (UnsupportedEncodingException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }

        usuario.setUsNome(txtNome4.getText());
        usuario.setUsEmail(txtEmail.getText());
        usuario.setUsCargo((String) comboCargo.getSelectedItem());
        usuario.setUsTelefone(formatoTelefone.getText());
        usuario.setUsUsuario(txtUsuario.getText());
        usuario.setUsStatus("A");
        if (passwordSenha.getText().equals(passwordSenhaConf.getText())) {
//            JOptionPane.showMessageDialog(this, "Senhas iguais");
        } else {
            JOptionPane.showMessageDialog(this, "Senhas não são iguais");
            passwordSenha.setText("");
            passwordSenhaConf.setText("");
            passwordSenha.requestFocus();
        }
        if (txtNome4.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Não preencheu o campo obrigatório: Nome.");
            return;
        }
        if (txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Não preencheu o campo obrigatório: E-mail.");
            return;
        }
        if (passwordSenha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Não preencheu o campo obrigatório: Senha.");
            return;
        }
        if (txtUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Não preencheu o campo obrigatório: Usuário.");
            return;
        }

        try {
            controle.salvarNovo(usuario);
            JOptionPane.showMessageDialog(null, "Usuário Salvo");
            dispose();
        } catch (Exception ex) {
            System.out.println(ex);
            //JOptionPane.showMessageDialog(null, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_botSalvarActionPerformed
