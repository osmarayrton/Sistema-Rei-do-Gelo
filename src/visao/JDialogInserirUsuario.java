    private void txtEmailKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtEmailKeyReleased
        // TODO add your handling code here:
        int opcao = -1;

        System.out.println(txtEmail.getText());
        List<Usuario> usuarioo = controle.consultarPorEmailUs(txtEmail.getText());

        if (!usuarioo.isEmpty()) {
            //           System.out.println("nao vazio");
            int contador = 0;
            int contador2 = 0;

            for (Usuario u : usuarioo) {
                if (u.getUsStatus().equals("A")) {
                    contador += 1;
                    this.usuario = u;
                } else if (u.getUsStatus().equals("I")) {
                    contador2 += 1;
                    this.usuario = u;
                }
            }

            if (contador > 0) {
                txtEmail.setBackground(Color.red);
                JOptionPane.showMessageDialog(this, "Já existe um cadastro para esse e-mail.");
                txtNome4.setText(usuario.getUsNome());
                txtEmail.setText(usuario.getUsEmail());
                txtUsuario.setText(usuario.getUsUsuario());
                comboCargo.setSelectedItem(usuario.getUsCargo());
                formatoTelefone.setText(usuario.getUsTelefone());
                passwordSenha.setText(usuario.getUsSenha());
                passwordSenhaConf.setText(usuario.getUsSenha());
            } else {
                txtEmail.setBackground(Color.white);
            }

            if (usuario.getUsStatus().equals("I")) {

                opcao = JOptionPane.showConfirmDialog(this, "Deseja reativar o usuário?", "Usuário inativo", 0);

                if (opcao == 0) {
                    System.out.println("Entrou recuperação de dados" + "");
//                    JOptionPane.showMessageDialog(this, "Já existe um cadastro para esse e-mail.");
                    txtEmail.setBackground(Color.red);
                    txtNome4.setText(usuario.getUsNome());
                    txtEmail.setText(usuario.getUsEmail());
                    txtUsuario.setText(usuario.getUsUsuario());
                    comboCargo.setSelectedItem(usuario.getUsCargo());
                    formatoTelefone.setText(usuario.getUsTelefone());
                    passwordSenha.setText(usuario.getUsSenha());
                    passwordSenhaConf.setText(usuario.getUsSenha());
                }
            }
        } else {
            System.out.println("vazio");
            txtEmail.setBackground(Color.white);
//                botSalvar.setEnabled(true);
        }


    }//GEN-LAST:event_txtEmailKeyReleased

