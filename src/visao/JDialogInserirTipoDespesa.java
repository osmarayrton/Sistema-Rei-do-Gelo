    private void botSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botSalvarActionPerformed
        // TODO add your handling code here:
//        tipoDespesa.setTpdespCod(Integer.parseInt(txtCodigo.getText()));
        List<TipoDespesa> listTipoDesp = controle.listarTodos();
        int i = 0;

        tipoDespesa.setTpdespDescricao(txtTipo.getText());
        for (TipoDespesa tipoDespesa1 : listTipoDesp) {
            if (tipoDespesa1.getTpdespDescricao().toUpperCase().equals(tipoDespesa.getTpdespDescricao().toUpperCase())) {
                i++;
            }
        }
        if (i == 0) {
            tipoDespesa.setTpdespStatus("A");
            controle.salvarNovo(tipoDespesa);
            JOptionPane.showMessageDialog(null, "Tipo de Despesa Salva");
        }
        dispose();
    }//GEN-LAST:event_botSalvarActionPerformed

