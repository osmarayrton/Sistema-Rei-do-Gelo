    private void tabDespesasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabDespesasMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int linha = tabDespesas.getSelectedRow();
            
            if(linha != -1){
                System.out.println("Entrou no 2 if");
                Despesa desp = despesaList.get(linha);
                JDialogInserirDespesas tela = new JDialogInserirDespesas(null, true, desp);
                 tela.setVisible(true);
                 
                 despesaList.clear();
                 despesaList.addAll(controle.listarTodos());
            }
            
        }
    }//GEN-LAST:event_tabDespesasMouseClicked

