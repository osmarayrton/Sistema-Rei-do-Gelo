    private void botPesquisarFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botPesquisarFuncActionPerformed
        // TODO add your handling code here:
        int linha = -1;
        linha = tabDespesas.getSelectedRow();

        if (linha != -1) {
            despesa = listaDesp.get(linha);
            txtBuscarDesp.setText(despesa.getDespDescricao());
        }

        listaDesp.clear();
        listaDesp.addAll(controle.consultarPorNome(txtBuscarDesp.getText()));
        System.out.println(listaDesp.size());
        despesaList.clear();
        despesaList.addAll(listaDesp);

    }//GEN-LAST:event_botPesquisarFuncActionPerformed

